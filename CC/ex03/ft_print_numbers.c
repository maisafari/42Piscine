#include<unistd.h>

void	ft_print_numbers(void)
{
   	int num;
	char c_num;
	num = 0;
	
	while (num <= 9)
 	{
	 	c_num = num +'0';
	 	write(1,&c_num,1);
		num++;
	}
}
